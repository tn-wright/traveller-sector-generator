function comparePlanetsByCoords(a, b) {
  if (a.xCoord === b.xCoord) {
    return a.yCoord - b.yCoord
  }

  return a.xCoord - b.xCoord
}

function generateSector() {
  //Generate all of the planets into the subsectorMatrix and the planet array
  planetLib.generatePlanetLocations(subsectorDensities, subsectorMatrix, planetLib.planets)

  //Sort the planet array based of the population and tech level of the planets
  planetLib.planets.sort(planetLib.comparePlanetsByPopAndTech)

  //Generate trade routes between planets
  tradeLib.generateTradeRoutes(planetLib.planets, subsectorMatrix)

  //Sort the planets by coordinates. This is used for display on the page
  planetLib.planets.sort(comparePlanetsByCoords)

  //evenPlanets = planetLib.planets.slice(0, Math.ceil(planetLib.planets.length / 2))
  //oddPlanets = planetLib.planets.slice(-Math.ceil(planetLib.planets.length / 2))
}

